package beans;

public class TemporadaBean {
    private int idTemporada;
    private int idSerie;
    private int numeroTemp;
    private int anoTemp;

    public int getIdTemporada() {
        return idTemporada;
    }

    public void setIdTemporada(int idTemporada) {
        this.idTemporada = idTemporada;
    }

    public int getIdSerie() {
        return idSerie;
    }

    public void setIdSerie(int idSerie) {
        this.idSerie = idSerie;
    }

    public int getNumeroTemp() {
        return numeroTemp;
    }

    public void setNumeroTemp(int numeroTemp) {
        this.numeroTemp = numeroTemp;
    }

    public int getAnoTemp() {
        return anoTemp;
    }

    public void setAnoTemp(int anoTemp) {
        this.anoTemp = anoTemp;
    }
}

